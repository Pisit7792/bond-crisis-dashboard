"""
explain.py — คำอธิบายภาษาคน (v2)

หลักการออกแบบ:
1. เป็น rule-based ล้วน (ไม่ใช้ AI แต่งข้อความ) → คำอธิบายคงที่ ตรวจสอบได้ ทดสอบได้
2. ทุกคำอธิบายต้องมี 3 ส่วน: คืออะไร / วิธีอ่าน / สิ่งที่มัน *ไม่ได้* บอก
   (ส่วนที่สามคือหัวใจ — กันการตีความเกินจริง ตามหลัก "ไม่ขายฝัน")
3. เกณฑ์โซน (เช่น <30 = สงบ) เป็น "ค่าตั้งต้นของแดชบอร์ดนี้" ไม่ใช่มาตรฐานสากล
   และต้องบอกผู้ใช้ตรงๆ แบบนั้น
"""
from __future__ import annotations

import math


def _ok(x) -> bool:
    return x is not None and not (isinstance(x, float) and math.isnan(x))


def _pct(x) -> str:
    return f"{x:.0f}" if _ok(x) else "ไม่มีข้อมูล"


# ---------------------------------------------------------------------------
# โซนของ composite score — ค่าตั้งต้น (ปรับได้) ไม่ใช่มาตรฐานสากล
# ---------------------------------------------------------------------------

def composite_zone(score: float) -> tuple[str, str]:
    """คืน (ชื่อโซน, ความหมายสั้น) — เกณฑ์ตั้งต้นของแดชบอร์ดนี้เท่านั้น"""
    if not _ok(score):
        return ("ไม่มีข้อมูล", "คำนวณคะแนนไม่ได้")
    if score < 30:
        return ("สงบ", "ตัวชี้วัดส่วนใหญ่อยู่ระดับปกติเมื่อเทียบอดีต")
    if score < 60:
        return ("ปานกลาง", "มีบางตัวเริ่มตึงตัว ควรติดตามแต่ยังไม่ผิดปกติ")
    if score < 80:
        return ("ตึงตัว", "หลายตัวชี้วัดสูงกว่าอดีตชัดเจน ควรตรวจความเสี่ยงพอร์ต")
    return ("ตึงตัวมาก", "ตัวชี้วัดส่วนใหญ่อยู่โซนสุดขั้วเทียบประวัติ")


SUBMODEL_PLAIN = {
    "curve": "เส้น yield curve (สัญญาณเตือนล่วงหน้าตัวเดียวในชุดนี้)",
    "stress": "ดัชนีความเครียดการเงิน (วัด 'ตอนนี้')",
    "credit": "ส่วนต่างดอกเบี้ยหุ้นกู้เสี่ยงสูง (วัด 'ตอนนี้')",
    "vol": "ความผันผวนของตลาด (วัด 'ตอนนี้')",
    "breadth": "สัดส่วนสินทรัพย์เสี่ยงที่หลุดแนวโน้มขาขึ้น",
}


# ---------------------------------------------------------------------------
# บทสรุปภาษาคน (deterministic) — ใช้บนแท็บภาพรวม
# ---------------------------------------------------------------------------

def plain_summary(composite: float, subs: dict, spread: float, rec_prob: float,
                  inv_days: int, n_warnings: int, is_demo: bool,
                  vol_name: str = "MOVE") -> str:
    zone, zone_desc = composite_zone(composite)
    lines: list[str] = []

    if is_demo:
        lines.append("🧪 **โหมด DEMO:** ตัวเลขทั้งหมดเป็นข้อมูลสังเคราะห์เพื่อทดลอง"
                     "หน้าจอ — บทสรุปนี้จึงไม่มีความหมายจริง ห้ามใช้ตัดสินใจ")

    # ย่อหน้า 1: ตอนนี้เป็นอย่างไร
    if _ok(composite):
        used = {k: v for k, v in subs.items() if _ok(v)}
        if used:
            hi_k = max(used, key=used.get)
            lo_k = min(used, key=used.get)
            lines.append(
                f"**ตอนนี้เป็นอย่างไร:** คะแนนรวม {composite:.0f}/100 = โซน "
                f"**{zone}** ({zone_desc}) ตัวที่ดันคะแนนขึ้นมากสุดคือ"
                f"{SUBMODEL_PLAIN[hi_k]} ({used[hi_k]:.0f}/100) "
                f"ส่วนที่สงบสุดคือ{SUBMODEL_PLAIN[lo_k]} ({used[lo_k]:.0f}/100) "
                f"— มีสัญญาณเตือนเข้าเกณฑ์ {n_warnings} รายการ")
    else:
        lines.append("**ตอนนี้เป็นอย่างไร:** ข้อมูลไม่พอคำนวณคะแนนรวม")

    # ย่อหน้า 2: ตัวเลขนี้ "แปลว่า" อะไรกันแน่
    p2 = ("**คะแนนนี้แปลว่าอะไรกันแน่:** มันคือ 'เทอร์โมมิเตอร์' วัดความตึง"
          "ของตลาด ณ วันนี้เทียบกับอดีต — ไม่ใช่ 'หมอดู' ที่ทำนายอนาคต "
          "คะแนนสูงไม่ได้แปลว่าวิกฤตจะเกิด และคะแนนต่ำไม่ได้แปลว่าปลอดภัย")
    if _ok(spread) and spread < 0:
        p2 += (f" | ที่ต้องรู้เพิ่ม: เส้น yield curve กำลัง 'พลิกกลับหัว' "
               f"(inverted{f' ต่อเนื่อง {inv_days} วันทำการ' if inv_days else ''}) "
               "— ในอดีตมักนำหน้าเศรษฐกิจถดถอย แต่ช้าได้ถึง 6-24 เดือน "
               "และเคยเตือนผิดมาแล้ว ใช้จับจังหวะซื้อขายไม่ได้")
    if _ok(rec_prob):
        x = round(rec_prob * 100)
        p2 += (f" | ความน่าจะเป็น recession {x}% อ่านแบบง่าย: ในอดีตเมื่อ curve "
               f"หน้าตาแบบวันนี้ ราว {x} ใน 100 ครั้งตามด้วยถดถอยภายใน ~1 ปี "
               f"(อีก {100-x} ครั้งไม่เกิด)")
    lines.append(p2)

    # ย่อหน้า 3: ข้อจำกัดที่ต้องจำ
    lines.append(
        "**ข้อควรจำ 3 ข้อ:** (1) สัญญาณเตือนแนวนี้ในงานวิจัยผิดราวครึ่งหนึ่ง "
        "(เตือน 2 ครั้ง เกิดจริง ~1 ครั้ง) (2) ตัวชี้วัดเกือบทั้งหมดวัด 'ตอนนี้' "
        "ไม่ใช่ 'อนาคต' (3) วิกฤตพันธบัตรจริง 4 ครั้งล่าสุด (2013/2020/2022/2023) "
        "เกิดจากหนี้และ leverage ที่ซ่อนอยู่ ซึ่งแดชบอร์ดราคาแบบนี้มองไม่เห็น"
        "ล่วงหน้า | *เกณฑ์โซน (สงบ/ปานกลาง/ตึงตัว) เป็นค่าตั้งต้นของแดชบอร์ดนี้ "
        "ปรับได้ ไม่ใช่มาตรฐานสากล*")

    return "\n\n".join(lines)


