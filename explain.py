def interpret_percentile(pct: float, name: str = "ค่านี้") -> str:
    if not _ok(pct):
        return f"{name}: ไม่มีข้อมูลพอคำนวณ"
    if pct >= 95:
        lvl = "สูงผิดปกติมาก (โซนสุดขั้ว)"
    elif pct >= 80:
        lvl = "สูงกว่าปกติชัดเจน"
    elif pct >= 60:
        lvl = "ค่อนไปทางสูง"
    elif pct >= 40:
        lvl = "ระดับกลางๆ ของประวัติ"
    else:
        lvl = "ต่ำกว่าค่าเฉลี่ยในอดีต (โซนสงบ)"
    return (f"{name}อยู่ที่เปอร์เซ็นไทล์ {pct:.0f} = สูงกว่า {pct:.0f}% "
            f"ของค่าที่เคยเกิดในอดีต → {lvl} "
            "(เปอร์เซ็นไทล์บอกว่า 'วันนี้เทียบอดีตเป็นอย่างไร' — ไม่ได้บอกว่าพรุ่งนี้จะไปทางไหน)")


def interpret_zscore(z: float, name: str = "ค่านี้") -> str:
    if not _ok(z):
        return f"{name}: ไม่มีข้อมูลพอคำนวณ"
    d = abs(z)
    if d < 1:
        lvl = "อยู่ในช่วงปกติ"
    elif d < 2:
        lvl = "เริ่มห่างจากปกติ"
    else:
        lvl = "ห่างจากปกติมาก (เกิดไม่บ่อยในอดีต)"
    side = "สูงกว่า" if z > 0 else "ต่ำกว่า"
    return (f"{name}มี z-score {z:+.1f} = {side}ค่าเฉลี่ยในอดีตประมาณ {d:.1f} "
            f"'ขั้นความผันผวนปกติ' → {lvl}")


def curve_explainer(rec_prob: float, spread: float, inv_days: int) -> str:
    md = ["**Yield curve คืออะไร (ภาษาคน):** เทียบดอกเบี้ยพันธบัตรรัฐบาลระยะยาว "
          "(10 ปี) กับระยะสั้น (3 เดือน) ปกติระยะยาวควรให้ดอกเบี้ยสูงกว่า "
          "(รอเงินนาน ต้องได้มากกว่า) — เมื่อไหร่ที่ระยะสั้น *สูงกว่า* ระยะยาว "
          "เรียกว่า 'พลิกกลับหัว (inverted)' แปลว่าตลาดกำลังคาดว่าดอกเบี้ย/"
          "เศรษฐกิจข้างหน้าจะแย่ลง"]
    if _ok(spread):
        if spread < 0:
            md.append(f"**ตอนนี้:** ส่วนต่าง {spread:+.2f}% = **กลับหัวอยู่**"
                      + (f" (ต่อเนื่อง {inv_days} วันทำการ)" if inv_days else ""))
        else:
            md.append(f"**ตอนนี้:** ส่วนต่าง {spread:+.2f}% = ยังปกติ (ไม่กลับหัว)")
    if _ok(rec_prob):
        x = round(rec_prob * 100)
        md.append(f"**เลข {x}% อ่านยังไง:** ในอดีต เมื่อ curve หน้าตาแบบวันนี้ "
                  f"ประมาณ {x} ใน 100 ครั้งตามด้วยเศรษฐกิจถดถอยภายใน ~1 ปี "
                  f"(และอีก {100-x} ครั้ง *ไม่เกิด* — ไม่ใช่คำพยากรณ์แน่นอน)")
    md.append("**สิ่งที่มัน *ไม่ได้* บอก:** จังหวะเวลา (ช้าได้ 6-24 เดือน) "
              "ใช้จับจังหวะเทรดไม่ได้, เคยเตือนผิดมาแล้วในอดีต, และมันทำนาย "
              "'เศรษฐกิจถดถอย' ไม่ใช่ 'วิกฤตสภาพคล่องพันธบัตร' — วิกฤตปี "
              "2013/2020/2022 ไม่ได้ถูกเตือนล่วงหน้าด้วยตัวนี้")
    return "\n\n".join(md)


