def regime_explainer(p_high: float, sig_lo_bp: float, sig_hi_bp: float) -> str:
    md = ["**Regime คืออะไร (ภาษาคน):** โมเดลแบ่ง 'สภาพอากาศ' ของตลาดพันธบัตร"
          "เป็น 2 แบบจากข้อมูลจริง — ช่วง 'ลมสงบ' (ดอกเบี้ยขยับสัปดาห์ละประมาณ "
          f"{sig_lo_bp:.0f} จุด) กับช่วง 'พายุ' (ขยับประมาณ {sig_hi_bp:.0f} จุด)"
          if _ok(sig_lo_bp) and _ok(sig_hi_bp) else
          "**Regime คืออะไร (ภาษาคน):** โมเดลแบ่ง 'สภาพอากาศ' ของตลาดพันธบัตร"
          "เป็นช่วงลมสงบกับช่วงพายุ จากขนาดการแกว่งของดอกเบี้ย"]
    if _ok(p_high):
        md.append(f"**ตอนนี้:** โมเดลให้ความน่าจะเป็น {p_high:.0%} ว่าอยู่ช่วง 'พายุ' "
                  "(อ่านจากเส้นประ = มุมมอง real-time)")
    md.append("**ทำไมมี 2 เส้น:** เส้นทึบ (smoothed) คือการมองย้อนหลังเมื่อรู้ข้อมูล"
              "ทั้งหมดแล้ว — คมชัดแต่ *ใช้ตอนเกิดเหตุจริงไม่ได้* ส่วนเส้นประ "
              "(filtered) คือสิ่งที่รู้ได้จริง ณ เวลานั้น ช่องว่างระหว่างสองเส้นคือ "
              "'ความช้าของการรู้ตัว' ที่มีจริงเสมอ")
    md.append("**สิ่งที่มัน *ไม่ได้* บอก:** พายุลูกหน้าจะมาเมื่อไหร่ — งานวิจัยชี้ว่า"
              "โมเดลแบบนี้ยืนยัน regime *หลัง* เปลี่ยนไปแล้ว ใช้เป็นบริบท "
              "ไม่ใช่ปุ่มซื้อขาย")
    return "\n\n".join(md)


def trend_explainer(breadth: float) -> str:
    md = ["**ตารางนี้อ่านยังไง:**",
          "- **เทียบ 200DMA:** ราคาปัจจุบันอยู่เหนือหรือใต้ค่าเฉลี่ย ~1 ปีของตัวเอง "
          "— 'เหนือ' = แนวโน้มใหญ่ยังขึ้น, 'ใต้' = แนวโน้มใหญ่เสีย (บอกอดีตถึง"
          "ปัจจุบัน ไม่ใช่อนาคต)",
          "- **โมเมนตัม 12 เดือน:** ปีที่ผ่านมาราคาเปลี่ยนกี่ % — บวก = วิ่งมาแล้ว "
          "ไม่ได้แปลว่าจะวิ่งต่อ",
          "- **Realized vol:** ช่วงนี้แกว่งแรงแค่ไหน (ตัวเลขสูง = เหวี่ยงแรง = "
          "โอกาสทั้งกำไร/ขาดทุนใหญ่ขึ้นทั้งคู่)"]
    if _ok(breadth):
        md.append(f"- **Breadth {breadth:.0f}%:** สินทรัพย์เสี่ยงที่ติดตาม "
                  f"{breadth:.0f}% กำลังอยู่ใต้เส้นแนวโน้ม — ยิ่งสูง ตลาดยิ่ง "
                  "risk-off เป็นวงกว้าง")
    md.append("**สิ่งที่ตารางนี้ *ไม่ใช่*:** สัญญาณซื้อขาย — หลักฐานวิชาการ: "
              "นักเทรดรายวันน้อยกว่า 3 ใน 100 คนที่ทำกำไรได้สม่ำเสมอหลังหักต้นทุน "
              "ตารางนี้มีไว้เป็น 'บริบท' ว่าตลาดอยู่โหมดไหนเท่านั้น")
    return "\n\n".join(md)


